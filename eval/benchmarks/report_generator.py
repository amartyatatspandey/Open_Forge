"""Markdown report generator for benchmark results."""

from __future__ import annotations

from pathlib import Path
from eval.benchmarks.task_schema import BenchmarkReport
from eval.benchmarks.tasks import BENCHMARK_TASKS


def generate_report(report: BenchmarkReport, output_path: Path | None = None) -> str:
    """Generate a Markdown benchmark report.

    Args:
        report:      BenchmarkReport from run_benchmark().
        output_path: If provided, write the report to this path.

    Returns:
        Markdown string.
    """
    lines = [
        f"# OpenForge Search Controller Benchmark Report",
        f"",
        f"**Pipeline:** {report.pipeline_label}  ",
        f"**Run timestamp:** {report.run_timestamp}  ",
        f"**Tasks:** {report.tasks_run}  ",
        f"**Attempts per task:** {report.n_attempts}  ",
        f"",
        f"## Summary",
        f"",
        f"| Metric | Value |",
        f"|---|---|",
        f"| Pass@1 | {report.pass_at_1 * 100:.1f}% |",
        f"| Pass@{report.n_attempts} | {report.pass_at_n * 100:.1f}% |",
        f"| Mean ERC score (attempt 1) | {report.mean_erc_score:.4f} |",
        f"| Failed tasks | {len(report.failed_tasks)} |",
        f"",
        f"## By Difficulty",
        f"",
        f"| Difficulty | Pass@1 |",
        f"|---|---|",
    ]

    for diff in ["simple", "medium", "hard"]:
        val = report.by_difficulty.get(diff, 0.0)
        lines.append(f"| {diff.capitalize()} | {val * 100:.1f}% |")

    lines += [
        f"",
        f"## Per-Task Results (Attempt 1)",
        f"",
        f"| Task ID | Difficulty | ERC Score | Passed | Error |",
        f"|---|---|---|---|---|",
    ]

    tasks_by_id = {t.task_id: t for t in BENCHMARK_TASKS}

    # Group results by task, show attempt 1
    attempt1_by_task: dict[str, "TaskResult"] = {}
    for r in report.task_results:
        if r.attempt == 1:
            attempt1_by_task[r.task_id] = r

    for task_id, result in sorted(attempt1_by_task.items()):
        task = tasks_by_id.get(task_id)
        diff = task.difficulty if task else "unknown"
        passed_str = "✅" if result.passed else "❌"
        error_str = result.error[:40] + "..." if result.error else "-"
        lines.append(
            f"| {task_id} | {diff} | {result.erc_score:.4f} | "
            f"{passed_str} | {error_str} |"
        )

    if report.failed_tasks:
        lines += [
            f"",
            f"## Failed Tasks",
            f"",
        ]
        for task_id in report.failed_tasks:
            task = tasks_by_id.get(task_id)
            prompt = task.prompt[:80] + "..." if task and len(task.prompt) > 80 else (task.prompt if task else "")
            lines.append(f"- **{task_id}:** {prompt}")

    lines += ["", "---", "*Generated by eval/benchmarks/report_generator.py*"]
    markdown = "\n".join(lines)

    if output_path is not None:
        output_path.parent.mkdir(parents=True, exist_ok=True)
        output_path.write_text(markdown, encoding="utf-8")

    return markdown
